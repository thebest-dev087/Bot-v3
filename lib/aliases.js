// Inverte para lookup rápido: { "banir": "ban", "kick": "ban", ... }
const reverseMap = {};
for (const [canon, list] of Object.entries(ALIASES)) {
  for (const a of list) reverseMap[a.toLowerCase()] = canon;
}

function resolve(cmd) {
  if (!cmd) return null;
  return reverseMap[cmd.toLowerCase()] || cmd.toLowerCase();
}

function totalAliases() {
  return Object.values(ALIASES).reduce((s, arr) => s + arr.length, 0);
}

function totalCanonical() {
  return Object.keys(ALIASES).length;
}

module.exports = { ALIASES, resolve, totalAliases, totalCanonical, reverseMap };
